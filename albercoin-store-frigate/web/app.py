def env(name, default=""):
    return os.environ.get(name, default)


def load_settings():
    data = DEFAULTS.copy()
    if SETTINGS_FILE.exists():
        try:
            data.update(json.loads(SETTINGS_FILE.read_text()))
        except Exception:
            pass
    return data


def validate(data):
    errors = []
    if data.get("computeBackend") not in ("AUTO", "GPU", "CPU"):
        errors.append("computeBackend debe ser AUTO, GPU o CPU")
    patterns = {
        "cacheSize": r"^[1-9][0-9]*[KMG]$",
        "memoryLimit": r"^[1-9][0-9]*(MB|GB)$",
        "dbThreads": r"^[1-9][0-9]?$",
        "batchSize": r"^[1-9][0-9]{3,8}$",
        "rpcBatchSize": r"^[1-9][0-9]{0,3}$",
        "rpcRequestTimeoutSeconds": r"^[1-9][0-9]{0,4}$",
        "maxLabels": r"^[1-9][0-9]{0,3}$",
        "maxSubscriptions": r"^[1-9][0-9]{0,4}$",
        "tcpPort": r"^[1-9][0-9]{1,4}$",
    }
    for key, pattern in patterns.items():
        if not re.match(pattern, str(data.get(key, ""))):
            errors.append(f"{key} tiene formato inválido")
    port = int(data.get("tcpPort", 0)) if str(data.get("tcpPort", "")).isdigit() else 0
    if not 1 <= port <= 65535:
        errors.append("tcpPort fuera de rango")
    return errors


