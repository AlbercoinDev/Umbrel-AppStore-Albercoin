def save_settings(data):
    CONFIG_DIR.mkdir(parents=True, exist_ok=True)
    DATA_DIR.mkdir(parents=True, exist_ok=True)
    SETTINGS_FILE.write_text(json.dumps(data, indent=2, sort_keys=True))
    CONFIG_FILE.write_text(render_toml(data))
    LOG_FILE.touch(exist_ok=True)


def docker(method, path):
    req = f"{method} {path} HTTP/1.1\r\nHost: docker\r\nContent-Length: 0\r\nConnection: close\r\n\r\n".encode()
    s = socket.socket(socket.AF_UNIX, socket.SOCK_STREAM)
    s.settimeout(10)
    s.connect(DOCKER_SOCK)
    s.sendall(req)
    chunks = []
    while True:
        b = s.recv(4096)
        if not b:
            break
        chunks.append(b)
    s.close()
    raw = b"".join(chunks).decode("utf-8", "replace")
    status = raw.split("\r\n", 1)[0]
    return status


def docker_action(action):
    name = urllib.parse.quote(CONTAINER_NAME, safe="")
    if action == "restart":
        return docker("POST", f"/containers/{name}/restart?t=60")
    if action == "stop":
        return docker("POST", f"/containers/{name}/stop?t=60")
    if action == "start":
        return docker("POST", f"/containers/{name}/start")
    return "HTTP/1.1 400 Bad action"


def tail(path, lines=120):
    try:
        data = path.read_text(errors="replace").splitlines()[-lines:]
        return "\n".join(data)
    except Exception as e:
        return f"No se puede leer el log: {e}"


class Handler(BaseHTTPRequestHandler):
    def do_GET(self):
        if self.path.startswith("/config.toml"):
            self.send_text(CONFIG_FILE.read_text() if CONFIG_FILE.exists() else "", "text/plain")
            return
        self.page(load_settings(), "")

    def do_POST(self):
        length = int(self.headers.get("Content-Length", "0"))
        form = urllib.parse.parse_qs(self.rfile.read(length).decode())
        action = form.get("action", [""])[0]
        data = load_settings()
        if action == "preset":
            preset = form.get("profile", ["balanced"])[0]
            data.update(PRESETS.get(preset, PRESETS["balanced"]))
            data["profile"] = preset
            save_settings(data)
            self.page(data, f"Perfil {preset} aplicado. Reinicia Frigate para usarlo.")
            return
        if action in ("restart", "stop", "start"):
            try:
                result = docker_action(action)
            except Exception as e:
                result = f"ERROR Docker: {e}"
            self.page(data, result)
            return
        if action == "save_restart" or action == "save":
            for key in FIELDS:
                if key in form:
                    data[key] = form[key][0].strip()
            errors = validate(data)
            if errors:
                self.page(data, " | ".join(errors))
                return
            save_settings(data)
            msg = "Configuración guardada."
            if action == "save_restart":
                try:
                    msg += " " + docker_action("restart")
                except Exception as e:
                    msg += f" ERROR Docker: {e}"
            self.page(data, msg)
            return
        self.page(data, "Acción no válida")

    def send_text(self, content, ctype="text/html"):
        body = content.encode()
        self.send_response(200)
        self.send_header("Content-Type", ctype + "; charset=utf-8")
        self.send_header("Content-Length", str(len(body)))
        self.end_headers()
        self.wfile.write(body)

